function criaObjeto (vetor) {
  let cesta = {};
  for (let i = 0; i < vetor.length; i += 1){
    let key = vetor[i];
    if (!cesta[key]) {
      cesta[key] = 0;
    }
    cesta[key] += 1;
  }
  imprime(cesta);
}

function imprime (cesta) {
  let summaries = [];
  for (let frutas in cesta) {
    let message = `${cesta[frutas]} ${frutas}`;
    if (cesta[frutas] > 1) message += 's';
    summaries.push(message);
  }
  console.log(`Sua cesta possui: ${summaries.join(', ')}.`);
}

